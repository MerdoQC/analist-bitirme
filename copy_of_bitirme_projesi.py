# Katılım Oranı (AttendanceRate) vs. Akademik Başarı

# 1. Katılım oranlarını mantıklı gruplara ayıralım
def attendance_bucket(rate):
    if rate < 0.85: return '1. Düşük (<%85)'
    elif rate < 0.95: return '2. Orta (%85-95)'
    else: return '3. Yüksek (>%95)'

dff['Attendance_Level'] = dff['AttendanceRate'].apply(attendance_bucket)

# 2. Katılım seviyelerine göre tüm başarı metriklerinin ortalamasını alalım
score_analysis = dff.groupby('Attendance_Level').agg({
    'GPA': 'mean',
    'TestScore_Math': 'mean',
    'TestScore_Reading': 'mean',
    'TestScore_Science': 'mean'
}).reset_index()

