# Çalışma Alışkanlıkları (StudyHours) ve Katılım Arasındaki İlişki

# 1. Çalışma saatlerini gruplara ayıralım (Örn: 0-1 saat, 1-2 saat, 2+ saat)
def study_group(hours):
    if hours < 1: return 'Düşük (0-1 sa)'
